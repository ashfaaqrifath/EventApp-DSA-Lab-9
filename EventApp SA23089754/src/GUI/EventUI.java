    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        if (seatList.getHeapSize() > 0) {
        Member nextMember = seatList.remove(); // This removes the root (highest priority member)
        JOptionPane.showMessageDialog(this, "Next Ticket:\nName: " + nextMember.getName() + 
                                      "\nID: " + nextMember.getNumber());
    } else {
        JOptionPane.showMessageDialog(this, "No more tickets available.");
    }
    }//GEN-LAST:event_btnNextActionPerformed

