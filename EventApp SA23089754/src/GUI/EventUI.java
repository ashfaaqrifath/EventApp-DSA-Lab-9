    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            if (count <= 200) {
                Member m = new Member(txtName.getText(), Integer.parseInt(txtId.getText()));
                seatList.insert(m);
                count++;
                JOptionPane.showMessageDialog(this, "Member Added");
            } else {
                JOptionPane.showMessageDialog(this, "Sorry List is Full");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter correct member ID");
        }
        txtName.setText("");
        txtId.setText("");
    }//GEN-LAST:event_btnAddActionPerformed

